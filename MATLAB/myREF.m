function Kref = myREF(R, L, Km, Bm, I, Kw, Ki)

%---System Matrices----------------------------------------------
%---| x = Ax + Bu |----------------------------------------------
%---| y = Cx      |----------------------------------------------
A = [-Bm/I Km/I; -Km/L -R/L];
B = [0; 1/L];
C = [1 0];
D = 1;
K = [Kw Ki];

Kref = -D*inv(C*inv(A+B*K)*B)